package ejercicio03_08;

public class Punto {
    
    private int circuloId;
    private int rectanguloId;
    private String referencia;

    public int getCirculoId() {
        return circuloId;
    }

    public void setCirculoId(int circuloId) {
        this.circuloId = circuloId;
    }

    public int getRectanguloId() {
        return rectanguloId;
    }

    public void setRectanguloId(int rectanguloId) {
        this.rectanguloId = rectanguloId;
    }

    public String getReferencia() {
        return referencia;
    }

    public void setReferencia(String referencia) {
        this.referencia = referencia;
    }

}
